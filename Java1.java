import java.io.PrintWriter;
public class MyClass {
    public static void main(String args[]) {
      int x;
      int y;
      int z;
    
      PrintWriter pw = new PrintWriter(System.out);    
        
      while(true){
        x=0;
        y=1;
        
        do {
            System.out.println("\n");
            pw.print(x);
            pw.flush();
            
            z = x + y;
            x = y;
            y =z;
        } while (x < 255);
        break;
      }
      
    }
}
